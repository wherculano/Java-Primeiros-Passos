public class TestaLacosEncadeados {
    public static void main(String[] args) {
        // Tabuada
        for(int n1 = 1; n1 <=10; n1++){
            for(int n2 = 1; n2<=10; n2++){
                System.out.println(n1 + " x " + n2 + " = " + n1*n2);
            }
            System.out.println("---------------");
        }
    }
}
